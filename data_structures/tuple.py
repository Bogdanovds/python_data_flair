"""
Кортеж - неизменяемый тип данных. Он может содержать последовательность элементов.
Последовательность элементов в кортеже может быть разных типов.
"""

# 1. Создание
procentages = (90, 95, 89)

# 2. Кортеж можно создать без круглых скобок, это называется - упаковка кортежа
b = 1, 2.0, 'three'

# 3. Распаковка кортежа - присваивание значений кортежа последовательности переменных
procentages = (0, 1, 2, 3, 4, 5)
a, b, c, d, e, f = procentages

# 4. Создание кортежа с одним элементом
