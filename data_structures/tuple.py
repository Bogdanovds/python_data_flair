procentages[1]

# 6. Нарезка кортежа, положительная и отрицательная индексация
# (0, 1, 2, 3, 4, 5)
procentages[2:4]
# (2, 3)
procentages[:4]
# (0, 1, 2, 3)
procentages[4:]
# (4, 5)
procentages[2:2]
